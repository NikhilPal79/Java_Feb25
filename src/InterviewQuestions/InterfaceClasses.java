    /// (what to be done) and no implementation details (how to be done) of these methods. In this way, interfaces
    /// satisfy the definition of abstraction. The implementation techniques of the methods declared in an interface are
    /// totally up to the classes implementing that interface.

    /// 3.An interface can be thought of as a contract that a class has to fulfill while implementing an interface.
    /// According to this contract, the class that implements an interface has to @Override all the abstract methods
    /// declared in that very interface.



    /// Key Features:
    ///✅ Methods are abstract by default (until Java 8).

    ///✅ All methods are public and abstract (unless they are static or default).

    ///✅ Variables are public, static, and final by default.

    ///✅ A class uses the implements keyword to implement an interface.

    ///✅ Java supports multiple interface implementation, unlike classes (no multiple inheritance with classes
